package java_projects;

public class Datatypes {

	public static void main(String[] args) {
		int a = 10;
		short a1 = 10;
		byte a2 = 1;
		long a3 = 100;
		
		float f1 = 1;
		double f2 = 1;
		
		char s = 'f';
		String s11 = "ascii";
		
		boolean b = 5 > 6;
		
		System.out.println(String.format("%d", a));
		System.out.println(String.format("%d", a1));
		System.out.println(String.format("%d", a2));
		System.out.println(String.format("%d", a3));
		System.out.println(String.format("%f", f1));
		System.out.println(String.format("%e", f2));
		System.out.println(String.format("%s", s));
		System.out.println(String.format("%s", s11));
		System.out.println(String.format("%b", b));
		

	}

}
